local M = {
--[[ 	"goolord/alpha-nvim",
	dependencies = { "kyazdani42/nvim-web-devicons" },
	event = "VimEnter",
	config = function()
		local alpha = require("alpha")
		local dashboard = require("alpha.themes.dashboard")
		-- Add tips to footer of the dashboard
		local job = require("plenary.job")
		job:new({
			command = "curl",
			args = { "-s", "-m 3", "https://vtip.43z.one" },
			on_exit = function(j, exit_code)
				local res = table.concat(j:result())
				if exit_code ~= 0 then
					res = "Error fetching tip: " .. res
				end
				dashboard.section.footer.val = res
				vim.schedule(function()
					alpha.redraw()
				end)
			end,
		}):start()
		
		dashboard.section.buttons.val = {
			dashboard.button(", f f", "Find File                 ", ":Telescope find_files<cr>"),
			dashboard.button(", f o", "Recents                   ", ":Telescope oldfiles<cr>"),
			dashboard.button(", f p", "Projects                  ", ":Telescope projects<cr>"),
			dashboard.button(", f g", "Find Word                 ", ":Telescope live_grep<cr>"),
			dashboard.button(", b m", "Bookmarks                 ", ":Telescope marks<cr>"),
			dashboard.button(
				", s r",
				"Load Last Session         ",
				":lua require('auto-session').RestoreSession()<cr>"
			),
		}

		alpha.setup(dashboard.opts)
		-- {icon = "  ", desc = "Find File                 ", key = ", f f", action = "Telescope find_files"},
		-- {icon = "  ", desc = "Recents                   ", key = ", f o", action = "Telescope oldfiles"  },
		-- {icon = "  ", desc = "Projects                  ", key = ", f p", action = "Telescope projects"  },
		-- {icon = "  ", desc = "Find Word                 ", key = ", f g", action = "Telescope live_grep" },
		-- {icon = "洛 ", desc = "New File                  ", key = ", f n", action = "DashboardNewFile"    },
		-- {icon = "  ", desc = "Bookmarks                 ", key = ", b m", action = "Telescope marks"     },
		-- {icon = "  ", desc = "Load Last Session         ", key = ", s r", action = "RestoreSession"      }
	end, ]]
}

